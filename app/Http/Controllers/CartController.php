    public function create($id)
    {
        // dd($id);
        $carts = Cart::where('user_id',auth()->user()->id,'product_id',$id)->get()->all();
        $cart = new Cart();
        $cart->user_id= auth()->user()->id;
        $cart->product_id= $id;
        $cart->qty= 1;
        $cart->price= 0;
        $cart->save();
        return redirect()->route('user.cart.index');
    }

